    public function post_comment(Request $request,$id)
    {
        $user = Auth::user();
        $post = Post::findOrfail($id);
        $comment = new Comment();
        $comment->user_id = $user->id;
        $comment->post_id = $post->id;
        $comment->content = $request['content'];
        $comment->save();
        //dd($comment);
       return redirect()->back();
    }
